""" Listas 
Una lista es una estructura de datos y tipo de dato en python con caracteristicas especiales. 
Lo cual nos permiten almacenar cualquier tipo de valor como enteros, cadenas y hasta otras funciones.

"""

lista1 = [1,2,3,4,5,6] # para asignar una lista se usan los cochertes 

""" Metodos Listas """

lista1.append() # Agrega un elemento al final de la lista 
lista1.remove() # Elimina el primer elemento que encuentra en la lista con el valor dado. Regresa None
lista1.pop() # elimina elementos
lista1.clear() # vacia las listas
lista1.sort() # ordena la lista
lista1.copy() # copia una lista
lista1.count() # cuenta el numero de veces que se encuentra un elemento
lista1.index() # regresa el indice
lista1.insert() # agrega un elemnto segun el indice
lista1.extend() # combinar los elementos de otra lista
